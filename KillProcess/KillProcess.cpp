// CKillProcessApp 초기화

BOOL CKillProcessApp::InitInstance()
{
	// 응용 프로그램 매니페스트가 ComCtl32.dll 버전 6 이상을 사용하여 비주얼 스타일을
	// 사용하도록 지정하는 경우, Windows XP 상에서 반드시 InitCommonControlsEx()가 필요합니다.
	// InitCommonControlsEx()를 사용하지 않으면 창을 만들 수 없습니다.
	INITCOMMONCONTROLSEX InitCtrls;
	InitCtrls.dwSize = sizeof(InitCtrls);
	// 응용 프로그램에서 사용할 모든 공용 컨트롤 클래스를 포함하도록
	// 이 항목을 설정하십시오.
	InitCtrls.dwICC = ICC_WIN95_CLASSES;
	InitCommonControlsEx(&InitCtrls);

	CWinApp::InitInstance();


	AfxEnableControlContainer();

	// 대화 상자에 셸 트리 뷰 또는
	// 셸 목록 뷰 컨트롤이 포함되어 있는 경우 셸 관리자를 만듭니다.
	CShellManager *pShellManager = new CShellManager;

	// 표준 초기화
	// 이들 기능을 사용하지 않고 최종 실행 파일의 크기를 줄이려면
	// 아래에서 필요 없는 특정 초기화
	// 루틴을 제거해야 합니다.
	// 해당 설정이 저장된 레지스트리 키를 변경하십시오.
	// TODO: 이 문자열을 회사 또는 조직의 이름과 같은
	// 적절한 내용으로 수정해야 합니다.
	SetRegistryKey(_T("로컬 응용 프로그램 마법사에서 생성된 응용 프로그램"));

	/// 동일한 프로세스가 실행 되지 않도록 코드 추가 by 윤경섭 2020.04.29
	HANDLE hMutexOneInstance = ::CreateMutex( NULL, TRUE, _T("AFX_JWIS_KILLPROC_8DFF6919_58B7_11D6_8719_0008C79F2040")); 
	bool AlreadyRunning = (GetLastError() == ERROR_ALREADY_EXISTS);

	if(hMutexOneInstance != NULL) 
		::ReleaseMutex(hMutexOneInstance); 

	if(AlreadyRunning) {
		//::MessageBox(NULL, _T("이미 프로그램이 실행중입니다."), _T("지역제어기"), MB_OK);
		return FALSE;
	}


	if(GetModuleFileName(NULL, CGlobal::Program_PATH, 256) != 0) {                // 이 프로그램의 PATH를 구한다.
		*(_tcsrchr(CGlobal::Program_PATH, _T('\\'))+1) = '\0';                           // FULL_PATH에서 프로그램 이름을 제거한 순수 PATH를 구한다.
		_stprintf_s(CGlobal::PROGRAM_INI_FULLPATH,_countof(CGlobal::PROGRAM_INI_FULLPATH), "%s%s", CGlobal::Program_PATH, _T("KILLPROCESS.INI"));
	}
	else
	{
		::MessageBox(NULL, _T("프로그램위치를 구할수 없습니다."), _T("지역제어기"), MB_OK);
		return FALSE;
	}

	CKillProcessDlg dlg;
	m_pMainWnd = &dlg;
	INT_PTR nResponse = dlg.DoModal();
	if (nResponse == IDOK)
	{
		// TODO: 여기에 [확인]을 클릭하여 대화 상자가 없어질 때 처리할
		//  코드를 배치합니다.
	}
	else if (nResponse == IDCANCEL)
	{
		// TODO: 여기에 [취소]를 클릭하여 대화 상자가 없어질 때 처리할
		//  코드를 배치합니다.
	}

	// 위에서 만든 셸 관리자를 삭제합니다.
	if (pShellManager != NULL)
	{
		delete pShellManager;
	}

	// 대화 상자가 닫혔으므로 응용 프로그램의 메시지 펌프를 시작하지 않고  응용 프로그램을 끝낼 수 있도록 FALSE를
	// 반환합니다.
	return FALSE;
}

